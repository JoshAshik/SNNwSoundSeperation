"""
librimix_dataset.py — Pre-generated Libri2Mix dataset loader for v7 SNN-TasNet.

Expected directory structure (generated by LibriMix/scripts/create_librimix_from_metadata.py):
    {root}/train-100/mix_clean/*.wav  |  s1/*.wav  |  s2/*.wav  — 13,900 pairs
    {root}/dev/mix_clean/*.wav        |  s1/*.wav  |  s2/*.wav  —  3,000 pairs
    {root}/test/mix_clean/*.wav       |  s1/*.wav  |  s2/*.wav  —  3,000 pairs

All files: 16 kHz, mono, variable length (Libri2Mix "max" mode pads to the longer source).
Invariant: mix_clean[i] = s1[i] + s2[i] for all i (holds exactly by construction).

Returns (mixture, s1, s2) — shape (clip_len,) each, peak-normalised using the mixture peak.
Because s1 and s2 share the mixture's peak scale, mixture = s1 + s2 is preserved after
normalisation — the reconstruction loss in the training script can rely on this.
"""

import math
import os
import random
import sys
from pathlib import Path
from typing import List, Tuple

import torch
import torch.nn.functional as F
import torchaudio
from torch import Tensor
from torch.utils.data import DataLoader, Dataset

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")

DEFAULT_SR       = 16_000
DEFAULT_DURATION = 4.0
DEFAULT_CLIP_LEN = int(DEFAULT_SR * DEFAULT_DURATION)   # 64,000 samples


# ─────────────────────────────────────────────────────────────────────────────
# Low-level helpers
# ─────────────────────────────────────────────────────────────────────────────

def _load_wav(path: str) -> Tensor:
    """Load a 16 kHz mono wav. Returns (N,) float32."""
    wav, sr = torchaudio.load(path)
    if sr != DEFAULT_SR:
        raise ValueError(f"Expected {DEFAULT_SR} Hz, got {sr} Hz: {path}")
    return wav.mean(0).float()


def _crop_or_pad_aligned(
    mix: Tensor, s1: Tensor, s2: Tensor, target_len: int
) -> Tuple[Tensor, Tensor, Tensor]:
    """
    Apply an identical crop/pad to mix, s1, s2 so that mix = s1 + s2 still holds.

    If length >= target_len: random-crop at the same start offset for all three.
    If length <  target_len: tile-repeat all three then trim to target_len.
    """
    n = mix.shape[0]
    if n >= target_len:
        start = random.randint(0, n - target_len)
        return (mix[start : start + target_len],
                s1 [start : start + target_len],
                s2 [start : start + target_len])
    reps = math.ceil(target_len / n)
    return (mix.repeat(reps)[:target_len],
            s1 .repeat(reps)[:target_len],
            s2 .repeat(reps)[:target_len])


# ─────────────────────────────────────────────────────────────────────────────
# Spike-safe augmentation  (identical rules to two_speaker_dataset.py)
# ─────────────────────────────────────────────────────────────────────────────

def spike_safe_augment(
    mixture: Tensor, s1: Tensor, s2: Tensor, L: int
) -> Tuple[Tensor, Tensor, Tensor]:
    """
    Augmentations safe for SNN (LIF) inputs — no abrupt discontinuities.

    Every operation applied to mixture is also applied to s1 and s2 so that
    PIT targets stay aligned and mixture = s1 + s2 is preserved.
    """
    # 1. Gain jitter ±6 dB
    if random.random() < 0.8:
        gain = 10 ** (random.uniform(-6.0, 6.0) / 20.0)
        mixture, s1, s2 = mixture * gain, s1 * gain, s2 * gain

    # 2. Polarity inversion
    if random.random() < 0.5:
        mixture, s1, s2 = -mixture, -s1, -s2

    # 3. Circular time shift ≤ 0.75 s
    if random.random() < 0.5:
        shift = random.randint(1, L // 4)
        mixture = torch.roll(mixture, shift)
        s1      = torch.roll(s1,      shift)
        s2      = torch.roll(s2,      shift)

    # 4. Low-level additive noise on mixture only (targets stay clean for PIT)
    if random.random() < 0.6:
        level = random.uniform(0.0, 0.015)
        rms   = mixture.pow(2).mean().sqrt().clamp(min=1e-8)
        mixture = mixture + torch.randn_like(mixture) * level * rms

    # 5. Smooth cosine amplitude taper
    if random.random() < 0.3:
        taper_len = random.randint(L // 16, L // 8)
        start     = random.randint(0, L - taper_len)
        t         = torch.linspace(0.0, 2.0 * math.pi, taper_len)
        envelope  = torch.ones(L)
        envelope[start : start + taper_len] = 0.5 - 0.5 * torch.cos(t)
        mixture, s1, s2 = mixture * envelope, s1 * envelope, s2 * envelope

    return mixture, s1, s2


# ─────────────────────────────────────────────────────────────────────────────
# Dataset
# ─────────────────────────────────────────────────────────────────────────────

class LibriMixDataset(Dataset):
    """
    Loads pre-generated Libri2Mix wav triples (mix_clean, s1, s2).

    Each __getitem__ call:
      1. Loads all three wavs (they share the same length by construction).
      2. Applies an aligned crop/pad to the fixed clip_len (preserves mix=s1+s2).
      3. Peak-normalises using the mixture peak (applied uniformly to all three).
      4. Applies spike-safe augmentation if augment=True.

    Returns (mixture, s1, s2) — all (clip_len,) float32.
    """

    def __init__(
        self,
        root:     str,
        split:    str   = "train-100",
        clip_len: int   = DEFAULT_CLIP_LEN,
        augment:  bool  = True,
    ) -> None:
        split_dir = Path(root) / split
        mix_dir   = split_dir / "mix_clean"
        s1_dir    = split_dir / "s1"
        s2_dir    = split_dir / "s2"

        for d in (mix_dir, s1_dir, s2_dir):
            if not d.is_dir():
                raise RuntimeError(f"Directory not found: {d}")

        stems = sorted(p.stem for p in mix_dir.glob("*.wav"))
        if not stems:
            raise RuntimeError(f"No .wav files in {mix_dir}")

        self.triplets: List[Tuple[str, str, str]] = [
            (str(mix_dir / f"{s}.wav"),
             str(s1_dir  / f"{s}.wav"),
             str(s2_dir  / f"{s}.wav"))
            for s in stems
        ]
        self.clip_len = clip_len
        self.augment  = augment

    def __len__(self) -> int:
        return len(self.triplets)

    def __getitem__(self, idx: int) -> Tuple[Tensor, Tensor, Tensor]:
        mix_path, s1_path, s2_path = self.triplets[idx]

        mix = _load_wav(mix_path)
        s1  = _load_wav(s1_path)
        s2  = _load_wav(s2_path)

        mix, s1, s2 = _crop_or_pad_aligned(mix, s1, s2, self.clip_len)

        # Peak-normalise using mixture peak — scale is shared so mix = s1 + s2 holds
        peak = mix.abs().max().clamp(min=1e-8)
        mix, s1, s2 = mix / peak, s1 / peak, s2 / peak

        if self.augment:
            mix, s1, s2 = spike_safe_augment(mix, s1, s2, self.clip_len)

        return mix, s1, s2


# ─────────────────────────────────────────────────────────────────────────────
# DataLoader factory
# ─────────────────────────────────────────────────────────────────────────────

def build_librimix_dataloaders(
    librimix_root: str,
    train_split:   str = "train-100",
    val_split:     str = "dev",
    clip_len:      int = DEFAULT_CLIP_LEN,
    batch_size:    int = 8,
    num_workers:   int = 8,
    train_augment: bool = True,
) -> Tuple[DataLoader, DataLoader]:
    """
    Build train and val DataLoaders from a Libri2Mix root directory.

    Args:
        librimix_root: Path containing split subdirectories (train-100/, dev/, test/).
        train_split:   Subdirectory name for training data (default: "train-100").
        val_split:     Subdirectory name for validation data (default: "dev").
        clip_len:      Fixed waveform length in samples (crop/pad applied).
        batch_size:    Batch size for both loaders.
        num_workers:   DataLoader worker processes.
        train_augment: Whether to apply spike_safe_augment on training data.

    Returns:
        (train_loader, val_loader)
    """
    train_ds = LibriMixDataset(librimix_root, train_split, clip_len, augment=train_augment)
    val_ds   = LibriMixDataset(librimix_root, val_split,   clip_len, augment=False)

    train_loader = DataLoader(
        train_ds,
        batch_size  = batch_size,
        shuffle     = True,
        num_workers = num_workers,
        pin_memory  = True,
        drop_last   = True,
        persistent_workers = num_workers > 0,
    )
    val_loader = DataLoader(
        val_ds,
        batch_size  = batch_size,
        shuffle     = False,
        num_workers = num_workers,
        pin_memory  = True,
        drop_last   = False,
        persistent_workers = num_workers > 0,
    )

    return train_loader, val_loader


